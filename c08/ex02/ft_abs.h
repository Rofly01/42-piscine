#ifndef FT_ABS_H
# define FT_ABS_H

# define ABS(Value) \
({ \
 	typeof(Value) nb = (Value); \
	if (nb < 0) \
		nb *= -1; \
	nb; \
})

#endif /* FT_ABS_H */
