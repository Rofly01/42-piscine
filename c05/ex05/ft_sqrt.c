#include <stdio.h>

int	ft_sqrt(int nb)
{
	long	i;

	i = 1;
	if (nb == 1)
		return (nb);
	while (i <= (nb / 2))
	{
		if ((i * i) == nb)
			return (i);
		i++;
	}
	return (0);
}


int	main(void)
{
	printf("%d", ft_sqrt(898411908));

}
